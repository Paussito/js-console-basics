//For knowing the data type of a variable we use "typeof"
let var1 = "text" //The first Variable
let var2 = 2 //The second variable
let var3 //We don't put anything and it will be "Undefined"
let var4 = true //The boolean one, you can change it for false

//Starting using "typeof", for using it is very simple. We put "typeof [the variable name]"
typeof var1 //It will say "String"
typeof var2 //It will say "Number"
typeof var3 //It will say "Undefined"
typeof var4 //It will say "Boolean"
